from typing import List

import numpy as np
from scipy import spatial


def scalar(vec1, vec2):
    return -1 * (spatial.distance.cosine(np.array(vec1), np.array(vec2)) - 1)


def get_rec_vec(person, events) -> List[int]:
    """function for calculating recommendations for the user

    Args:
        person (Pers): the class of the user who needs to fill out recommendations
        events (List[Event]): list of events to recommend

    Returns:
        rec_vec (List[int]): vector containing indexes of events sorted by interests
    """
    rec_vec = {
        event.id: scalar(person.interest_vec[event.cluster], event.description_vec)
        for event in events
    }
    rec_vec = [k[0] for k in reversed(sorted(rec_vec.items(), key=lambda f: f[1]))]
    return rec_vec


def angle_between(v1, v2):
    """Returns the angle in radians between vectors 'v1' and 'v2'::

    >>> angle_between((1, 0, 0), (0, 1, 0))
    1.5707963267948966
    >>> angle_between((1, 0, 0), (1, 0, 0))
    0.0
    >>> angle_between((1, 0, 0), (-1, 0, 0))
    3.141592653589793
    """
    return np.arccos(
        np.clip(
            np.dot(
                np.array(v1) / np.linalg.norm(np.array(v1)),
                np.array(v2) / np.linalg.norm(np.array(v2)),
            ),
            -1.0,
            1.0,
        )
    )


